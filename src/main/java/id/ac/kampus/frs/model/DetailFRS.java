package id.ac.kampus.frs.model;

public class DetailFRS {
    private int idDetail;
    private int idFrs;
    private String kodeMk;

    public int getIdDetail() { return idDetail; }
    public void setIdDetail(int idDetail) { this.idDetail = idDetail; }

    public int getIdFrs() { return idFrs; }
    public void setIdFrs(int idFrs) { this.idFrs = idFrs; }

    public String getKodeMk() { return kodeMk; }
    public void setKodeMk(String kodeMk) { this.kodeMk = kodeMk; }
}
